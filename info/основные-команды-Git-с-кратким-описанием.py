# Основные команды Git с кратким описанием:
#
# 1. **`git init`**
#    Инициализирует новый репозиторий Git в текущей директории.
#
# 2. **`git clone <URL>`**
#    Клонирует удаленный репозиторий на ваш компьютер.
#
# 3. **`git status`**
#    Показывает текущее состояние репозитория: изменения, которые не добавлены в коммиты, файлы, которые были изменены, и т.д.
#
# 4. **`git add <file>`**
#    Добавляет изменения в индекс (staging area) для последующего коммита.
#
# 5. **`git commit -m "<сообщение>"`**
#    Фиксирует изменения, добавленные в индекс, с сообщением о коммите.
#
# 6. **`git push`**
#    Отправляет локальные изменения на удаленный репозиторий.
#
# 7. **`git pull`**
#    Забирает последние изменения из удаленного репозитория и объединяет их с вашим текущим состоянием.
#
# 8. **`git branch`**
#    Показывает все ветки в репозитории. Можно добавить имя ветки, чтобы создать новую.
#
# 9. **`git checkout <branch>`**
#    Переключает вас на указанную ветку.
#
# 10. **`git merge <branch>`**
#     Объединяет указанную ветку с текущей.
#
# 11. **`git log`**
#     Показывает историю коммитов с деталями: хеш коммита, автор, дата, сообщение коммита.
#
# 12. **`git reset --hard <commit>`**
#     Возвращает проект к указанному коммиту, удаляя все изменения после него.
#
# 13. **`git fetch`**
#     Забирает изменения с удаленного репозитория, но не объединяет их.
#
# 14. **`git stash`**
#     Временно сохраняет изменения, не фиксируя их, чтобы можно было переключиться на другую ветку.
#
# 15. **`git rebase`**
#     Переносит изменения из одной ветки на другую, изменяя базу текущей ветки.
